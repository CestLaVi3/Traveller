package com.chinasoft.model.main;

public class SysHref {
	private int hrefId;
   	private String hrefName;
   	private String hrefUrl;
   	private int count;
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getHrefId() {
		return hrefId;
	}
	public void setHrefId(int hrefId) {
		this.hrefId = hrefId;
	}
	public String getHrefName() {
		return hrefName;
	}
	public void setHrefName(String hrefName) {
		this.hrefName = hrefName;
	}
	public String getHrefUrl() {
		return hrefUrl;
	}
	public void setHrefUrl(String hrefUrl) {
		this.hrefUrl = hrefUrl;
	}
   
   	
}
